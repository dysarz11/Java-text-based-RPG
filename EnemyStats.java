public class EnemyStats {
    private int enemyHP;
    private int enemySTR;
    private int enemyDEX;
    private int enemyAGL;

    public void setEnemyHP(int hp) {
        enemyHP = hp;
    }

    public int getEnemyHP() {
        return enemyHP;
    }

    public void setEnemySTR(int str) {
        enemySTR = str;
    }

    public int getEnemySTR() {
        return enemySTR;
    }

    public void setEnemyDEX(int dex) {
        enemyDEX = dex;
    }

    public int getEnemyDEX() {
        return enemyDEX;
    }

    public void setEnemyAGL(int agl) {
        enemyAGL = agl;
    }

    public int getEnemyAGL() {
        return enemyAGL;
    }
}
